package aula01.exercicios;

public class IncrementoDec {
	public static void main(String[] args) {
		
		// valor numero inteiro 
		int contador = 10;
		
		// Exibir na tela
		
		System.out.println("valorIncial:" + contador);
		
		contador++;
		System.out.println("contador++:" + contador);
		
		contador--;
		System.out.println("contador--:" + contador);
		
	}

}
