package Aula03.pratica;

public class Progressao {
	public static void main(String[] args) {
		
		int contador = 1;
		
		//While enquanto
		while(contador >= 10) {
			System.out.print(contador  + ",");
			contador++;
			
			
		}
		//Do While
		do {
			System.out.print(contador  + ",");
			contador++;
		} while (contador<=10);
		
	}

}
